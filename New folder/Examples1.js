
// Declaring a variable using 'let'
let age = 25; // age is a variable that storesthe value 25
console.log("Age:", age); 

// Declaring a constant variable using 'const'
const name = "Anna"; // name is a constant variable that stores the string "Anna"
console.log("Name:", name); 

// Declaring a variable using 'var'
var city = "Ilishan"; // city is a variable that stores the string "Ilishan"
console.log("City:", city); 

// Reassigning a variable
age = 30; // age is updated to 30
console.log("Updated Age:", age); 

// Variables can hold different types of data
let isStudent = true; // isStudent is a boolean variable
console.log("Is Student:", isStudent); 